import { mkdir, readFile, writeFile } from 'node:fs/promises';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const root = resolve(dirname(fileURLToPath(import.meta.url)), '..');
const readJson = async (path) => JSON.parse(await readFile(resolve(root, path), 'utf8'));
export const normalizeText = (value) => value.replace(/\r\n/g, '\n');

export async function renderContracts() {
  const [states, events, tools] = await Promise.all([
    readJson('contracts/agent-states.json'),
    readJson('contracts/events.schema.json'),
    readJson('contracts/tools.schema.json')
  ]);
  const stateValues = states.enum;
  const eventValues = events.properties.type.enum;
  const characterValues = events.$defs.characterId.enum;
  const riskValues = tools.properties.risk_level.enum;
  const union = (values) => values.map((value) => `'${value}'`).join(' | ');

  const ts = `// Generated by scripts/generate-contracts.mjs. Do not edit.\n\n` +
`export type AgentState = ${union(stateValues)};\n` +
`export type AgentEventType = ${union(eventValues)};\n` +
`export type CharacterId = ${union(characterValues)};\n` +
`export type ToolRiskLevel = ${union(riskValues)};\n\n` +
`export interface ClientMessageData { content: string; character_id: CharacterId; }\n` +
`export interface AgentStateData { state: AgentState; }\n` +
`export interface AssistantDeltaData { delta: string; }\n` +
`export interface AssistantMessageData { content: string; }\n\n` +
`export interface ToolConfirmationDetails { target?: string; operation?: 'search' | 'read' | 'create' | 'replace' | 'clipboard'; content?: string | null; content_length?: number | null; will_create_backup?: boolean; }\n
export interface ToolConfirmationRequiredData { confirmation_id: string; tool_call_id: string; tool_name: string; risk_level: 'L2'; summary: string; expires_at: string; details?: ToolConfirmationDetails | null; }\n` +
`export interface ToolConfirmationResponseData { confirmation_id: string; approved: boolean; }\n` +
`export type ToolResultStatus = 'succeeded' | 'denied' | 'failed' | 'timed_out' | 'cancelled';\n` +
`export interface ToolResultData { tool_call_id: string; tool_name: string; status: ToolResultStatus; summary: string; }\n\n` +
`export interface AgentEvent<TData extends Record<string, unknown> = Record<string, unknown>> {\n` +
`  type: AgentEventType;\n  version: '1.0';\n  session_id: string;\n  request_id: string;\n  timestamp: string;\n  data: TData;\n}\n\n` +
`export interface AgentError {\n  error_code: string;\n  message: string;\n  recoverable: boolean;\n  request_id: string;\n  details?: Record<string, unknown>;\n}\n`;

  const pyList = (values) => values.map((value) => `    ${value.replaceAll('.', '_').replaceAll('-', '_').toUpperCase()} = "${value}"`).join('\n');
  const py = `# Generated by scripts/generate-contracts.mjs. Do not edit.\n` +
`from enum import Enum\nfrom typing import Any\n\nfrom pydantic import BaseModel, Field\n\n` +
`class AgentState(str, Enum):\n${pyList(stateValues)}\n\n` +
`class AgentEventType(str, Enum):\n${pyList(eventValues)}\n\n` +
`class CharacterId(str, Enum):\n${pyList(characterValues)}\n\n` +
`class ToolRiskLevel(str, Enum):\n${pyList(riskValues)}\n\n` +
`class ClientMessageData(BaseModel):\n    content: str = Field(min_length=1)\n    character_id: CharacterId\n\n` +
`class AgentStateData(BaseModel):\n    state: AgentState\n\n` +
`class AssistantDeltaData(BaseModel):\n    delta: str = Field(min_length=1)\n\n` +
`class AssistantMessageData(BaseModel):\n    content: str = Field(min_length=1)\n\n` +
`class ToolConfirmationRequiredData(BaseModel):\n    confirmation_id: str\n    tool_call_id: str\n    tool_name: str\n    risk_level: str = Field(pattern=r"^L2$")\n    summary: str\n    expires_at: str\n    details: dict[str, Any] | None = None\n\n` +
`class ToolConfirmationResponseData(BaseModel):\n    confirmation_id: str\n    approved: bool\n\n` +
`class ToolResultData(BaseModel):\n    tool_call_id: str\n    tool_name: str\n    status: str = Field(pattern=r"^(succeeded|denied|failed|timed_out|cancelled)$")\n    summary: str\n\n` +
`class AgentEvent(BaseModel):\n    type: AgentEventType\n    version: str = Field(pattern=r"^1\\.0$")\n    session_id: str\n    request_id: str\n    timestamp: str\n    data: dict[str, Any]\n\n` +
`class AgentError(BaseModel):\n    error_code: str\n    message: str\n    recoverable: bool\n    request_id: str\n    details: dict[str, Any] | None = None\n`;

  return new Map([
    ['generated/contracts.ts', ts],
    ['backend/app/schemas/contracts.py', py]
  ]);
}

export async function generate({ check = false } = {}) {
  const outputs = await renderContracts();
  let stale = false;
  for (const [relativePath, content] of outputs) {
    const path = resolve(root, relativePath);
    if (check) {
      const current = await readFile(path, 'utf8').catch(() => '');
      if (normalizeText(current) !== normalizeText(content)) {
        console.error(`Generated contract is stale: ${relativePath}`);
        stale = true;
      }
      continue;
    }
    await mkdir(dirname(path), { recursive: true });
    await writeFile(path, content, 'utf8');
    console.log(`Generated ${relativePath}`);
  }
  if (stale) process.exitCode = 1;
}

if (process.argv[1] && resolve(process.argv[1]) === fileURLToPath(import.meta.url)) {
  await generate({ check: process.argv.includes('--check') });
}